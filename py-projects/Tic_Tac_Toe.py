def clicked(b):
    global click, c
    if click == True and b["text"] == "":
        b.config(text="X", bg="red")
        c += 1
        click = False
        won()
    elif click == False and b["text"] == "":
        b.config(text="O", bg="blue")
        click = True
        c += 1
        won()
